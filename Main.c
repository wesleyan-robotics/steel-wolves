const float SPEED_LIMIT_FACTOR = 0.75;
/* The ratio between rotations from the small gear to the bigger gear */
const float ACCORDION_RATIO = 3.3921;

const int DPAD_UP = 0;
const int DPAD_DOWN = 4;

